    <h4>ECOA - Construindo sustentabilidade e democracia</h4>
    <p>A ECOA foi criada em 1989, em Campo Grande - MS, por um grupo de pesquisadores de diversas áreas profissionais, dentre elas as de biologia, comunicação, arquitetura, ciências sociais, engenharia e educação, para estabelecer um espaço de reflexão, debates e formulações e também desenvolver projetos e políticas para a conservação ambiental e a sustentabilidade tanto no meio urbano quanto no rural.</p>
    <p>Nesta perspectiva o Pantanal e a bacia hidrográfica do rio da Prata foram identificados como as regiões prioritárias, sendo que no caso Pantanal concentraram-se as ações de base comunitária, o que indica, também, uma das razões para a criação da organização.</p>
