                <h3>Sobre o INNBatível</h3>
                <p>
                    O <strong>INNBatível</strong> é primeiro site brasileiro de <strong>Compras Coletivas</strong> especializado em <strong>Turismo e Viagens</strong>. Fundado em 2010, em Florianópolis, ano em que nasceu a maioria dos concorrentes no Brasil, hoje o INNBatível é o único site de compras coletivas segmentado em turismo no estado de Santa Catarina, superando as turbulências do mercado. Em pouco mais de três anos, a empresa, que já nasceu focada no turismo, embarcou clientes para os mais diversos destinos nacionais e internacionais. Medalha de ouro no E-bit, com 91% de elogios, o INNBatível possui o maior ticket médio da categoria. De acordo com o Ranking Alexa, o INNBatível é o segundo site de compras coletivas focado em viagens e turismo mais acessado do país. Com o mote “Viaje mais, por menos”, a empresa também foi o primeiro site de compras coletivas a incluir aéreos nos seus pacotes de viagem.
